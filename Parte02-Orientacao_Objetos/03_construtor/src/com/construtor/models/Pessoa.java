package com.construtor.models;

public class Pessoa {
    public String nome;
    public String email;
    public String telefone;
    public String CPF;
    public int idade;
    public double altura;

    public Pessoa(String nome, String email, String telefone, String CPF, int idade, double altura) {
        this.nome = nome;
        this.email = email;
        this.telefone = telefone;
        this.CPF = CPF;
        this.idade = idade;
        this.altura = altura;
    }
    


}
