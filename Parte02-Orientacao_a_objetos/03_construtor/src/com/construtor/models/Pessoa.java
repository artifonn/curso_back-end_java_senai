package com.construtor.models;

public class Pessoa {
    // atributos
    public String nome;
    public String email;
    public String Telefone;
    public String cpf;
    public double altura;
    public int idade;

    public Pessoa() {
    }


    public Pessoa(String nome, String email, String Telefone, String cpf, double altura, int idade) {
        this.nome = nome;
        this.email = email;
        this.Telefone = Telefone;
        this.cpf = cpf;
        this.altura = altura;
        this.idade = idade;
    }
    







}
